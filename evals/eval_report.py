"""
Ghost Protocol — Evaluation report generator.

Reads evals/latest_results.json (written by eval_runner.py) and generates
a Markdown report saved to evals/latest_report.md.

Usage:
    python evals/eval_report.py
    python evals/eval_report.py --run   # run evals first, then generate report
"""

import sys
import json
import argparse
from pathlib import Path

_EVALS_DIR = Path(__file__).parent
_RESULTS_FILE = _EVALS_DIR / "latest_results.json"
_REPORT_FILE  = _EVALS_DIR / "latest_report.md"

_CATEGORY_LABELS = {
    "iq":      "Foundry IQ Retrieval",
    "routing": "Agent Routing",
    "phase":   "Phase Progression",
    "safety":  "Safety Filters",
}


def _grade(pct: float) -> str:
    if pct >= 100: return "PERFECT"
    if pct >= 80:  return "PASS"
    if pct >= 60:  return "MARGINAL"
    return "FAIL"


def _bar(passed: int, total: int, width: int = 20) -> str:
    filled = round(passed / total * width) if total else 0
    return "█" * filled + "░" * (width - filled)


def generate_report(data: dict) -> str:
    lines = []

    overall_pct = data["overall_pct"]
    grade       = _grade(overall_pct)
    run_at      = data["run_at"]

    lines += [
        "# Ghost Protocol — Evaluation Report",
        "",
        f"**Run:** {run_at}",
        f"**Overall score:** {data['total_passed']}/{data['total_cases']} "
        f"({overall_pct:.1f}%) — **{grade}**",
        "",
        "---",
        "",
        "## Summary by Category",
        "",
        "| Category | Passed | Total | Score | Grade |",
        "|----------|--------|-------|-------|-------|",
    ]

    for cat in data["categories"]:
        label = _CATEGORY_LABELS.get(cat["key"], cat["category"])
        g = _grade(cat["pct"])
        lines.append(
            f"| {label} | {cat['passed']} | {cat['total']} | "
            f"{cat['pct']:.0f}% `{_bar(cat['passed'], cat['total'])}` | {g} |"
        )

    lines += [
        "",
        "---",
        "",
        "## Results by Category",
        "",
    ]

    # Group results by category key
    by_cat: dict[str, list] = {}
    for r in data["results"]:
        by_cat.setdefault(r["category"], []).append(r)

    for key in ["iq", "routing", "phase", "safety"]:
        if key not in by_cat:
            continue
        label    = _CATEGORY_LABELS.get(key, key)
        cat_results = by_cat[key]
        cat_passed  = sum(1 for r in cat_results if r["passed"])
        cat_total   = len(cat_results)

        lines += [
            f"### {label}  ({cat_passed}/{cat_total})",
            "",
            "| ID | Status | Description | Detail |",
            "|----|--------|-------------|--------|",
        ]

        for r in cat_results:
            status = "✅ PASS" if r["passed"] else "❌ FAIL"
            detail = r.get("detail", "").replace("|", "\\|")
            lines.append(f"| `{r['id']}` | {status} | {r['description']} | {detail} |")

        lines.append("")

    lines += [
        "---",
        "",
        "## Interpretation",
        "",
        "| Grade | Threshold | Meaning |",
        "|-------|-----------|---------|",
        "| PERFECT | 100% | All cases pass — system fully correct |",
        "| PASS    | ≥ 80% | Acceptable for production / demo |",
        "| MARGINAL | ≥ 60% | Some gaps; review failing cases before demo |",
        "| FAIL    | < 60% | Critical issues present — investigate before judge review |",
        "",
        f"*Generated by `evals/eval_report.py` — Ghost Protocol hackathon eval suite.*",
    ]

    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser(description="Generate Ghost Protocol eval report")
    parser.add_argument("--run", action="store_true",
                        help="Run eval_runner first, then generate report")
    args = parser.parse_args()

    if args.run:
        sys.path.insert(0, str(_EVALS_DIR))
        from eval_runner import run_all
        data = run_all()
    else:
        if not _RESULTS_FILE.exists():
            print(f"No results file found at {_RESULTS_FILE}")
            print("Run `python evals/eval_runner.py` first, or use --run flag.")
            sys.exit(1)
        data = json.loads(_RESULTS_FILE.read_text())

    report = generate_report(data)
    _REPORT_FILE.write_text(report)
    print(f"Report saved → {_REPORT_FILE}")
    print()
    print(report)


if __name__ == "__main__":
    main()
